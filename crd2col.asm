ASSUME	CS:CGR,DS:CGR
CGR	GROUP	COD,DAT
COD	SEGMENT BYTE
ORG	100H
CRD2COLOR	PROC
	MOV	SP,100H
	MOV	AX,10H
	INT	10H
	LEA	BX,FR
	LEA	DI,SN
	MOV	DS:[BX+2],CS
	MOV	ES:[DI+2],CS
	CALL	CRD2COL
	MOV	BX,DI
	PUSH	ES
	POP	DS
	CALL	EGAWSPR
	MOV	AH,7
	INT	21H
	MOV	AX,3
	INT	10H
	MOV	AX,4C00H
	INT	21H
ENDP
INCLUDE CRD2COL.LIB
INCLUDE EGAWSPR.LIB
INCLUDE BCRD2COL.LIB
ENDS
DAT	SEGMENT BYTE

INCLUDE VIDEO.TBL
FR	DW	OFFSET CGR:BUF,0
	DW	2
	DW	16
	DW	0,0
	DB	1
	DB	0,0,0
SN	DW	OFFSET CGR:BUF1,0
	DW	0,0,80H,80H
	DB	0,0,0,0
BUF	DB	16	DUP	(0AAH)
BUF1	DB	16	DUP	(0)

ENDS
END     CRD2COLOR

