COD	SEGMENT BYTE
ORG	100H
TRY	PROC
	MOV	SP,100H
	LEA	SI,FN
	LEA	DX,BUF
	DEC	AX
	CALL	LOAD_FILE
	JC	TRYE
	OR	CL,CL
	JNZ	TRYE
	PUSH	CX
	MOV	AX,010H
	INT	10H
	LEA	SI,BUF
	POP	AX
	MOV	AL,'A'
	LEA	DI,SPR_
	MOV	DX,4
	MOV	BX,WORD PTR [INK]
	CALL	SYM2SPR
	MOV	AH,07
	INT	21H
	MOV	AX,3
	INT	10H
	MOV	AX,4C00H
	INT	21H
TRYE:
	MOV	AX,4C01H
	INT	21H
INCLUDE IO.LIB
;INCLUDE D:\WORK\LIBRARY\EGA\EGAWSPR.LIB
INCLUDE SYM2SPR.LIB
ENDP
ENDS
DAT	SEGMENT BYTE
FN	DB	'TRY.FNT',0
VIEW_	DB	'PAVEL SKRYLEV''S COMPUTER',0

SPRTBL	DW	COD:SPR_,0
	DW	0
	DW	8
	DW	128,8
	DW	640
	DB	4
	DB	0

INK	DB	4
PAPER	DB	5
SPR_	DB	(8*16)/2	  DUP	      (0)
BUF:
ENDS
END     TRY

